if __name__ == '__main__':
    print("5 и 6 =", 5 & 6)
    print("5 или 6 =", 5 | 6)
    print("5 вправо на 2 бита =", 5 >> 2)
    print("5 влево на 2 бита =", 5 << 2)

# объяснение результата:
#      5 = 00000101
#      6 = 00000110
# 
#  5 & 6 = 00000100 = 4
# 
#  5 | 6 = 00000111 = 7
# 
# 5 >> 2 = 00000001 = 1
# 
# 5 << 2 = 00010100 = 2**4 + 2**2 = 16 + 4 = 20
